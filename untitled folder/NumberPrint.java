


  // Q.N 1.	Write a Java application to print all the numbers from 300 to 200 (Note: not 200 to 300).


public class NumberPrint {

	public static void main(String[] args)
	                            {
		
		System.out.println("The Number from 300 to 200:  "  ); 
		
		
		for(int n=300; n>=200; n--)  //using for loop
		
		{
			
			
		 
				
		    
		System.out.println(  n ); 
		}
 
		
		}

}
